// ── Helper Functions ─────────────────────────────────────────────────────────

export function getStateNames(): string[] {
  return NIGERIA_STATES.map((s) => s.name);
}

export function getStateByName(name: string): NigeriaState | undefined {
  return NIGERIA_STATES.find((s) => s.name === name);
}

export function getLGAsForState(stateName: string): string[] {
  const state = getStateByName(stateName);
  return state ? state.lgas.map((l) => l.name) : [];
}

export function getCitiesForLGA(stateName: string, lgaName: string): string[] {
  const state = getStateByName(stateName);
  if (!state) return [];
  const lga = state.lgas.find((l) => l.name === lgaName);
  return lga ? lga.cities : [];
}

export function getStateCenterCoords(stateName: string): { lat: number; lng: number } {
  const state = getStateByName(stateName);
  return state ? { lat: state.lat, lng: state.lng } : { lat: 9.0579, lng: 7.4951 };
}
